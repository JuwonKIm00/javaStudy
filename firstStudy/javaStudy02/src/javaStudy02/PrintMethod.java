package javaStudy02;

public class PrintMethod {

	public static void main(String[] args) {
		
		//쌍 따옴표는 문자열(String) 을 알려주기 위해 사용한다
		System.out.println("반갑습니다");
		System.out.println("안녕");
		
		//작은 따옴표는 문자(char)을 알려주기 위해 사용한다 
		System.out.println('a');
		
		//println 은 줄 바꿈까지 해준다
		//print 는 줄바꿈을 포함하지 않는다 
		//printf는 형식을 지정하여 출력한다 
		
		System.out.print('a');
		
		System.out.printf("%d 는 숫자 1이다",1);
	}

}
